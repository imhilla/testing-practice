const calculator = {
  add: (a, b) => {
    const x = a + b;
    return x;
  },
  subtract: (a, b) => {
    const x = a - b;
    return x;
  },
  divide: (a, b) => {
    const x = a / b;
    return x;
  },
  multiply: (a, b) => {
    const x = a * b;
    return x;
  },
};
export default calculator;
